import { supabase } from "../../lib/supabase/client";

export async function getSections(pageId: string) {
  const { data, error } = await supabase
    .from("sections")
    .select("*")
    .eq("page_id", pageId)
    .order("sort_order");

  if (error) throw error;

  return data ?? [];
}

export async function createDefaultSections(pageId: string) {
  const defaults = [
    {
      type: "hero",
      sort_order: 1,
      config: {
        title: "Premium website generated by AgencyOS",
        subtitle: "Edit this content from the property panel and it will auto-save to Supabase.",
      },
    },
    {
      type: "features",
      sort_order: 2,
      config: {
        items: ["Premium Design", "Fast Performance", "SEO Ready"],
      },
    },
    {
      type: "contact",
      sort_order: 3,
      config: {
        phone: "+91 98765 43210",
        email: "hello@agencyos.ai",
      },
    },
  ];

  const { error } = await supabase
    .from("sections")
    .insert(defaults.map((section) => ({ page_id: pageId, ...section })));

  if (error) throw error;
}

export async function ensureDefaultSections(pageId: string) {
  const existingSections = await getSections(pageId);

  if (existingSections.length > 0) {
    return existingSections;
  }

  await createDefaultSections(pageId);

  return await getSections(pageId);
}
